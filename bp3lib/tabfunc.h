  f.open(path); if (!f) likelihood<realnum>::Error( 112, func_name, (void*) &path );
  f.ignore(10000, (int)'\n');
  for ( int i=0; i<dimbess1; i++ )
  {
	for ( int j=0; j<dimbess2; j++ )	f >> Bess0arr[i+j*dimbess1];
	f.ignore(10000, (int)'\n');
  }
